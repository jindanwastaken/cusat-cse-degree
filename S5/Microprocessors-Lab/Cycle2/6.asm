START:	LXI H, 4000H
	LXI D, 5000H
	LDA 3000H
	MOV C, A
	
ODD:	MOV A, M
	ANI 01H
	JZ SKIPODD
	MOV A, M
	STAX D

SKIPODD:	INX D
	INX H
	DCR C
	JNZ ODD

	LXI H, 4000H
	LXI D, 5000H
	LDA 3000H
	MOV C, A
	
EVEN:	MOV A, M
	ANI 01H
	JNZ SKIPEVEN
	MOV A, M
	STAX D

SKIPEVEN:	INX D
	INX H
	DCR C
	JNZ EVEN

	HLT
	
	